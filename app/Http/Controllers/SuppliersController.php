        $response = collect();
        
         foreach($Suppliers as $Supplier){
 
             $data=[
                 "id" => $Supplier->id,
                 "label" => ucfirst($Supplier->name),
                 "value" => strtolower($Supplier->name)
             ];
             $response->push($data);
         }
         return $response;
    }

